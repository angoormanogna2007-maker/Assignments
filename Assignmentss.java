public class Assignmentss {
    public static void main(String[] args){

      //Arithmetic operators
        int a = 8;
        int b = 10;

        System.out.println("a+b =" + (a+b));
        System.out.println("a-b =" +(a-b));
        System.out.println("a*b =" +(a*b));
        System.out.println("a/b =" +(a/b));
        System.out.println("a%b =" +(a%b));

    // Increment and Decrement operators
        System.out.println("a++ =" + (a++));
        System.out.println("++a =" + (++a));
 
        System.out.println("a-- =" + (a--));   
        System.out.println("--a =" + (--a));

    // Relational operators
        System.out.println("a==b =" + (a==b));
        System.out.println("a!=b =" + (a!=b));
        System.out.println("a<b =" + (a<b));
        System.out.println("a<=b =" + (a<=b));
        System.out.println("a>b =" + (a>b));
        System.out.println("a>=b =" + (a>=b));


    //Logical operators
        System.out.println("a&&b =" + (a==b)); 
        System.out.println("a||b =" + (a<b));
        System.out.println("a!b =" + (a<=b));

    //compound operators
        System.out.println("a+=b =" + (a+=b));
        System.out.println("a-=b =" + (a-=b));
        System.out.println("a*=b =" + (a*=b));
        System.out.println("a/=b =" + (a/=b));
        System.out.println("a%=b =" + (a%=b));

    }
}
