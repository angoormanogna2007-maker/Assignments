public class compound4 {
    public static void main(String[]args){
        int balance = 700;
        System.out.println(balance);
        balance +=100;
        System.out.println(balance);
        balance -=200;
        System.out.println(balance);
        balance *=500;
        System.out.println(balance);
        balance /=50;
        System.out.println(balance);
        balance %=600;


    }
}
