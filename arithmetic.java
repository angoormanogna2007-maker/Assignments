public class arithmetic {
    public static void main(String[] args){
        int a = 5;
        int b = 2;
        System.out.println(a+b);  //7
        System.out.println(a-b);  //3
        System.out.println(a*b);  //10
        System.out.println(a/b);  //2
        System.out.println(a%b);  //1

    }
}

